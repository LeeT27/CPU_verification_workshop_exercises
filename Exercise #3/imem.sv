module imem (
    input  logic [31:0] addr,
    output logic [31:0] instr
);

    logic [31:0] mem [0:1023];

    initial begin
        // -----------------------------
        // LOAD PHASE
        // -----------------------------
        mem[0] = 32'h00402183; // lw   x3, 4(x0)     -> x3 = mem[1] = 10

        // -----------------------------
        // I-TYPE ARITHMETIC
        // -----------------------------
        mem[1] = 32'h00518113; // addi x2, x3, 5     -> x2 = 15
        mem[2] = 32'hFFB10193; // addi x3, x2, -5    -> x3 = 10

        // -----------------------------
        // R-TYPE ARITHMETIC
        // -----------------------------
        mem[3] = 32'h00310233; // add  x4, x2, x3    -> x4 = 25
        mem[4] = 32'h404202B3; // sub  x5, x4, x4    -> x5 = 0

        // -----------------------------
        // MORE I-TYPE OPS
        // -----------------------------
        mem[5] = 32'h00A28313; // addi x6, x5, 10    -> x6 = 10
        mem[6] = 32'hFFF30393; // addi x7, x6, -1    -> x7 = 9

        // -----------------------------
        // FINAL R-TYPE COMBINATION
        // -----------------------------
        mem[7] = 32'h00638433; // add  x8, x7, x6    -> x8 = 19

        // -----------------------------
        // STORE RESULT
        // -----------------------------
        mem[8] = 32'h00802223; // sw x8, 4(x0)       -> mem[1] = 19
    end

    // word aligned fetch
    assign instr = mem[addr[11:2]];

endmodule